"""Commit message generation benchmark for GitBench."""

import logging
from pathlib import Path

from gitbench.benchmarks import Benchmark
from gitbench.harness.loader import FixtureLoader
from gitbench.harness.scorer import Scorer
from gitbench.harness.types import Fixture, Score
from gitbench.utils.git import GitExecutor

logger = logging.getLogger(__name__)


class CommitMessagesBenchmark(Benchmark):
    """Benchmark for evaluating LLM-generated commit messages.

    This benchmark provides a diff to a model and evaluates whether
    the generated commit message accurately describes the changes.
    """

    name = "commit_messages"
    description = "Generate a commit message from a git diff"

    def __init__(self):
        """Initialize the commit messages benchmark."""
        self._loader = FixtureLoader()
        self._scorer = Scorer()

    def load_fixtures(self) -> list[Fixture]:
        """Load all commit message fixtures.

        Returns:
            List of Fixture objects from the fixtures/commit_messages directory.

        Raises:
            FileNotFoundError: If the fixtures directory doesn't exist.
        """
        fixtures_dir = Path(__file__).parent.parent.parent / "fixtures" / "commit_messages"
        logger.info(f"Loading fixtures from: {fixtures_dir}")

        fixtures = self._loader.load_dir(str(fixtures_dir))
        logger.info(f"Loaded {len(fixtures)} fixtures")
        return fixtures

    def score(self, fixture: Fixture, model_output: str) -> Score:
        """Score a commit message against the expected value.

        Args:
            fixture: The fixture containing the expected commit message.
            model_output: The commit message generated by the model.

        Returns:
            A Score object with passed/failed status and similarity value.
        """
        return self._scorer.score(fixture, model_output)

    def setup_fixture(self, fixture: Fixture) -> tuple[GitExecutor, str]:
        """Set up a git repository for a fixture.

        Args:
            fixture: The fixture containing setup commands.

        Returns:
            A tuple of (GitExecutor, repo_path).

        Raises:
            RuntimeError: If setup commands fail.
        """
        executor = GitExecutor()
        repo_path = executor.setup_repo(f"fixture_{fixture.id}", fixture.setup)
        logger.debug(f"Set up fixture {fixture.id} at {repo_path}")
        return executor, repo_path

    def get_diff(self, repo_path: str) -> str:
        """Get the diff for the current repo state.

        Args:
            repo_path: Path to the git repository.

        Returns:
            The diff output as a string.
        """
        import subprocess

        result = subprocess.run(
            ["git", "diff", "--staged"],
            cwd=repo_path,
            capture_output=True,
            text=True,
        )

        if result.returncode != 0:
            logger.error(f"git diff failed: {result.stderr}")
            return ""

        return result.stdout or result.stderr

    def format_prompt(self, fixture: Fixture, diff: str) -> str:
        """Format the prompt with the fixture prompt and diff.

        Args:
            fixture: The fixture containing the base prompt.
            diff: The git diff to include in the prompt.

        Returns:
            The formatted prompt string.
        """
        return f"{fixture.prompt}\n\nGit diff:\n{diff}"